    public void fill(){
        ConexionBarco b = new ConexionBarco();
        Barco barco = new Barco();
        barco = null;
        Barco[] a = b.ConsultarBarco(barco);
        if (a != null) {
            for (Barco alu : a) {
               cmbBarco.addItem(alu.getNombre());
            }
        }
    }
    
public void seleccionarSocio(){
    TableSocio.addMouseListener(new MouseAdapter() {
        @Override
        public void mousePressed(MouseEvent Mouse_evt) {
            //To change body of generated methods, choose Tools | Templates.
            if (Mouse_evt.getClickCount() == 1) {
                txtIdSocios.setText(TableSocio.getValueAt(TableSocio.getSelectedRow(), 0).toString());
                txtNombre.setText(TableSocio.getValueAt(TableSocio.getSelectedRow(), 1).toString());
                txtApellido.setText(TableSocio.getValueAt(TableSocio.getSelectedRow(), 2).toString());
                TableSocio.getValueAt(TableSocio.getSelectedRow(), 3).toString();
            }
        
        }
        
    });
}

