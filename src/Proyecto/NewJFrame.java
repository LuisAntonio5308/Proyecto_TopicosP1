private void insertarSocio(){

    try {
        
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String nomBarco = cmbBarco.getSelectedItem().toString();
        IdSocio = this.idSocio();
        
        boolean insertado = this.controlador.insertarSocio(IdSocio++, nombre, apellido, nomBarco);
        
        if (insertado == true) {
            JOptionPane.showMessageDialog(null, "Datos Guardados");
            this.cancelar();
            TableSocio.setModel(this.controlador.socios());
        }else{
            JOptionPane.showMessageDialog(null, "Algo fallo al Insertar");
        }
        
    } catch (NumberFormatException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, "Falla");
        
    }
        }


private void actualizarSocio(){
    String nombre = txtNombre.getText();
    String apellido = txtApellido.getText();
    String nomBarco = cmbBarco.getSelectedItem().toString();
    
    int id = Integer.parseInt(txtIdSocios.getText());
    boolean actualizar = this.controlador.actualizarSocio(id, nombre, apellido, nomBarco);
    
    if (actualizar == true) {
            JOptionPane.showMessageDialog(null, "Datos Actualizados");
            this.cancelar();
            TableSocio.setModel(this.controlador.socios());
        }else{
            JOptionPane.showMessageDialog(null, "Algo fallo al Actualizar");
        }
    
    
}

private void eliminarSocio(){
    try {
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        
        int id = Integer.parseInt(txtIdSocios.getText());
        
    boolean eliminado = this.controlador.eliminarSocio(id, nombre, apellido, null);
    
        if (eliminado==true) {
            JOptionPane.showMessageDialog(null, "Datos Eliminados");
            this.cancelar();
            TableSocio.setModel(this.controlador.socios());
        }else{
        JOptionPane.showMessageDialog(null, "Algo fallo al eliminar");
        }
        
    } catch (NumberFormatException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, "El valor introducido no es un numero");
        
    }
}

private void cancelar(){
    txtApellido.setText("");
    txtNombre.setText("");
}
