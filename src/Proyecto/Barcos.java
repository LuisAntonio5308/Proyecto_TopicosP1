private void insertarBarco(){
    try {
        int matricula = Integer.parseInt(txtMatricula.getText());
        String nombre = txtNombre.getText();
        int amarre = Integer.parseInt(txtAmarre.getText());
        float cuota = Integer.parseInt(txtCuota.getText());
        
        int Idarco = this.idBarco();
        
        boolean insertado = this.controlador.insertarBarco(Idarco++, matricula, nombre, amarre, cuota);
        
        if (insertado == true) {
            JOptionPane.showMessageDialog(null, "Datos Guardados");
            this.cancelar();
            TableBarcos.setModel(this.controlador.barcos());
        }else{
            JOptionPane.showMessageDialog(null, "Algo fallo al Insertar");
        }
        
    } catch (NumberFormatException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, "Falla");
        
    }
        }


private void actualizarBarco(){
    int matricula = Integer.parseInt(txtMatricula.getText());
        String nombre = txtNombre.getText();
        int amarre = Integer.parseInt(txtAmarre.getText());
        float cuota = Integer.parseInt(txtCuota.getText());
    
    int id = Integer.parseInt(txtIdBarco.getText());
    boolean actualizar = this.controlador.actualizarBarco(id, matricula, nombre, amarre, cuota);
    
    if (actualizar == true) {
            JOptionPane.showMessageDialog(null, "Datos Actualizados");
            this.cancelar();
            TableBarcos.setModel(this.controlador.barcos());
        }else{
            JOptionPane.showMessageDialog(null, "Algo fallo al Actualizar");
        }
}


private void eliminarBarco(){
    try {
        int matricula = Integer.parseInt(txtMatricula.getText());
        String nombre = txtNombre.getText();
        int amarre = Integer.parseInt(txtAmarre.getText());
        float cuota = Float.parseFloat(txtCuota.getText());
        int id = Integer.parseInt(txtIdBarco.getText());
        
    boolean eliminado = this.controlador.eliminarBarco(id, matricula, nombre, amarre, cuota);
        if (eliminado==true) {
            JOptionPane.showMessageDialog(null, "Datos Eliminados");
            this.cancelar();
            TableBarcos.setModel(this.controlador.barcos());
        }else{
        JOptionPane.showMessageDialog(null, "Algo fallo al eliminar");
        }
        
    } catch (NumberFormatException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, "El valor introducido no es un numero");
    }
}


private void cancelar(){
    txtIdBarco.setText("");
    txtMatricula.setText("");
    txtNombre.setText("");
    txtAmarre.setText("");
    txtCuota.setText("");
}
    
    
    
