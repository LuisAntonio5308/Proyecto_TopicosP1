public void seleccionarBarco(){
    TableBarcos.addMouseListener(new MouseAdapter() {
        @Override
        public void mousePressed(MouseEvent Mouse_evt) {
            //To change body of generated methods, choose Tools | Templates.
            if (Mouse_evt.getClickCount() == 1) {
                
                txtIdBarco.setText(TableBarcos.getValueAt(TableBarcos.getSelectedRow(), 0).toString());
                txtMatricula.setText(TableBarcos.getValueAt(TableBarcos.getSelectedRow(), 1).toString());
                txtNombre.setText(TableBarcos.getValueAt(TableBarcos.getSelectedRow(), 2).toString());
                txtAmarre.setText(TableBarcos.getValueAt(TableBarcos.getSelectedRow(), 3).toString());
                txtCuota.setText(TableBarcos.getValueAt(TableBarcos.getSelectedRow(), 4).toString());
                
            }
        
        }
        
    });
}

